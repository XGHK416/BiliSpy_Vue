<template>
  <div class="login-container">
    <el-form ref="loginForm" :model="loginForm" :rules="loginRules" class="login-form" auto-complete="on" label-position="left">

      <div class="title-container">
        <h3 class="title">Bili Spy</h3>
      </div>

      <el-form-item prop="identityId">
        <span class="svg-container">
          <svg-icon icon-class="user" />
        </span>
        <el-input
          ref="identityId"
          v-model="loginForm.identityId"
          placeholder="用户id/邮箱/电话"
          name="identityId"
          type="text"
          tabindex="1"
          auto-complete="on"
        />
      </el-form-item>

      <el-form-item prop="credential">
        <span class="svg-container">
          <svg-icon icon-class="password" />
        </span>
        <!-- @keyup.enter.native回车事件 -->
        <el-input
          :key="passwordType"
          ref="credential"
          v-model="loginForm.credential"
          :type="passwordType"
          placeholder="密码"
          name="credential"
          tabindex="2"
          auto-complete="on"
          @keyup.enter.native="handleLogin"
        />
        <span class="show-pwd" @click="showPwd">
          <svg-icon :icon-class="passwordType === 'password' ? 'eye' : 'eye-open'" />
        </span>
      </el-form-item>

      <el-button :loading="loading" type="primary" style="width:100%;margin-bottom:20px;" @click.native.prevent="handleLogin">登录</el-button>

      <!-- <div class="tips">
        <span style="margin-right:20px;">username: admin</span>
        <span> password: any</span>
      </div> -->
      <div style="position:relative;float:right" class="otherOption">
        <el-button class="register-button" type="success" size="mini" @click="registerDialogVisible=true">
          注册
        </el-button>
      </div>
    </el-form>
    <el-dialog
      :visible.sync="registerDialogVisible"
      width="400px"
    >
      <RegisterSign />
    </el-dialog>
  </div>
</template>

<script>
// 引用外部规则
import { validUsername } from '@/utils/validate'
import RegisterSign from './components/RegisterSign'

export default {
  name: 'Login',
  components: {
    RegisterSign
  },
  data() {
    // 自定义验证规则
    const validateIdentityId = (rule, value, callback) => {
      if (!validUsername(value)) {
        callback(new Error('请输入账号'))
      } else {
        callback()
      }
    }
    const validateCredential = (rule, value, callback) => {
      if (value.length < 6) {
        callback(new Error('密码长度需要大于6'))
      } else {
        callback()
      }
    }
    return {
      registerDialogVisible: false,
      loginForm: {
        identityId: '',
        credential: '',
        identityType:''
      },
      loginRules: {
        identityId: [{ required: true, trigger: 'blur', validator: validateIdentityId }],
        credential: [{ required: true, trigger: 'blur', validator: validateCredential }]
      },
      loading: false,
      passwordType: 'password',
      redirect: undefined
    }
  },
  watch: {
    $route: {
      handler: function(route) {
        this.redirect = route.query && route.query.redirect
      },
      immediate: true
    }
  },
  methods: {
    showPwd() {
      if (this.passwordType === 'password') {
        this.passwordType = ''
      } else {
        this.passwordType = 'password'
      }
      this.$nextTick(() => {
        this.$refs.credential.focus()
      })
    },
    handleLogin() {
      // 固定写法
      this.$refs.loginForm.validate(valid => {
        if (valid) {
          var identityId = this.loginForm.identityId+''
          console.log(identityId)
          if(identityId.indexOf('@')!=-1){
            this.loginForm.identityType = 'EMAIL'
          }else{
            if(identityId.indexOf('user')!=-1||identityId.indexOf('mo')!=-1){
              this.loginForm.identityType = 'ORIGIN'
            }else{
              this.loginForm.identityType = 'PHONE'
            }
          }
          console.log(this.loginForm)
          // do something
          this.loading = true
          this.$store.dispatch('user/login', this.loginForm).then(() => {
            console.log('get it!!')
            this.$router.push({ path: this.redirect || '/' })
            this.loading = false
          }).catch(() => {
            this.loading = false
          })
        } else {
          console.log('error submit!!')
          return false
        }
      })
    }
  }
}
</script>

